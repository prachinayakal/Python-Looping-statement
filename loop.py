# Looping statement
# For loop
#Looping statements are used to repeat a block of code multiple times
print("This is for loop: ")
for i in range(5):
    print(i)
    
# While loop
# The while loop we can execute a set of statements as long as a condition is true.
print("This is while loop:")
count = 0
while count < 5:
    print(count)
    count += 1
